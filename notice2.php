                A empresa tem capacidade de produção anual de 319.200 contadores monofásicos e 16.800 contadores trifásicos, para atender o mercado nacional, números que vão contribuir para a redução da importação de contadores de água e luz. Frederico Makilanda, que é o presidente do Conselho de Administração da Hengye Electronics Indústria, acentuou que, num período de cinco anos, a empresa vai poder vender à ENDE mais de um milhão de contadores do sistema pré-pago.
            </p>

